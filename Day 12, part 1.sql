DROP TABLE IF EXISTS #source;

WITH source AS (
    SELECT ordinal,
           REPLACE(LEFT([value], CHARINDEX(' ', [value])-1), '?', '_') AS springs,
           SUBSTRING([value], CHARINDEX(' ', [value])+1, LEN([value])) AS groups
    FROM STRING_SPLIT(REPLACE(@input, CHAR(13), ''), CHAR(10), 1))

SELECT *, (SELECT SUM(CAST([value] AS tinyint)) FROM STRING_SPLIT(groups, ',')) AS counter_target
INTO #source FROM source;

CREATE UNIQUE CLUSTERED INDEX IX ON #source (ordinal);





--- Part 1:

WITH permutations AS (
    SELECT s.ordinal,
           CAST('' AS varchar(50)) AS springs,
           s.springs AS spring_template,
           CAST(NULL AS varchar(50)) AS groups,
           s.groups AS groups_target,
           counter_target,
           0 AS [counter]
    FROM #source AS s

    UNION ALL

    SELECT p.ordinal,
           CAST(p.springs+[next].spring AS varchar(50)) AS springs,
           p.spring_template,
           --- Every time we "close" a group, we add a number to the "groups" column:
           CAST((CASE --- The character "." with a non-zero counter:
                      WHEN [next].spring='.' AND p.[counter]>0
                      THEN ISNULL(p.groups+',', '')+CAST(p.[counter] AS varchar(50))
                      --- ... or if this ("#") is the last character:
                      WHEN LEN(p.springs)+1=LEN(p.spring_template) AND [next].spring='#'
                      THEN ISNULL(p.groups+',', '')+CAST(p.[counter]+1 AS varchar(50))
                      --- Otherwise, just inherit the "groups" string:
                      ELSE p.groups END) AS varchar(50)) AS groups,
           p.groups_target,
           p.counter_target,
           --- Increment the "counter" column if the current character is a "#", otherwise reset it:
           (CASE WHEN [next].spring='#' THEN p.[counter]+1 ELSE 0 END) AS [counter]
    FROM permutations AS p
    CROSS APPLY (
        --- The expected number of #s in the current or next group:
        VALUES (CAST(REPLACE(SUBSTRING(p.groups_target, ISNULL(LEN(p.groups)+2, 1), 2), ',', '') AS int))
        ) AS x(next_group)
    CROSS APPLY (
        --- The expected next character (or wildcard) in the template:
        SELECT SUBSTRING(p.spring_template, LEN(p.springs)+1, 1) AS spring
        WHERE --- Stop recursion if 1) we have the full string length
              LEN(p.springs)<LEN(p.spring_template)
              --- or 2) we've reached the total # count for the string
          AND p.[counter]<p.counter_target
